  /**
   * Creates a ACC (ACCIDENT) segment object that belongs to the given 
   * message.  
   */
  public ACC(Group parent, ModelClassFactory factory) {
    super(parent, factory);
    Message message = getMessage();
    try {
       this.add(TS.class, false, 1, 19, new Object[]{message});
       this.add(ID.class, false, 1, 2, new Object[]{message, new Integer(50)});
       this.add(ST.class, false, 1, 25, new Object[]{message});
    } catch (HL7Exception he) {
        HapiLogFactory.getHapiLog(this.getClass()).error("Can't instantiate " + this.getClass().getName(), he);
    }
  }

  /**
   * Returns ACCIDENT DATE/TIME (ACC-1).
   */
